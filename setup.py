def create_directories() -> None:
    """Create required directories."""
    dirs = ["data", "logs", "sessions", "llm/prompts"]

    for dir_path in dirs:
        path = Path(dir_path)
        if not path.exists():
            path.mkdir(parents=True, exist_ok=True)
            print(f"Created directory: {dir_path}")


async def test_telegram_connection(token: str, channel_id: str) -> bool:
    """Test Telegram bot connection."""
    try:
        from telegram import Bot
        from telegram.error import TelegramError

        bot = Bot(token=token)

        # Try to get bot info
        me = await bot.get_me()
        print(f"Bot connected: @{me.username}")

        # Try to get chat info
        try:
            chat = await bot.get_chat(channel_id)
            print(f"Channel access confirmed: {chat.title or chat.username}")
            return True
        except TelegramError as e:
            print(f"Warning: Could not access channel: {e}")
            print("Make sure the bot is added as admin to the channel.")
            return False

    except ImportError:
        print("Warning: python-telegram-bot not installed. Skipping connection test.")
        return True
    except Exception as e:
        print(f"Error: {e}")
        return False


async def run_setup_wizard() -> None:
    """Run the interactive setup wizard."""
    print_header("TG AI Poster Setup Wizard")

    print("""
Welcome! This wizard will help you configure your autonomous
Telegram channel manager. You'll need:

  1. A Telegram bot token (from @BotFather)
  2. Your channel ID or username
  3. An OpenAI API key (or Anthropic/DeepSeek)

Let's get started!
""")

    config = {}

    # Telegram configuration
    print_section("Telegram Bot Configuration")

    print("""
To create a Telegram bot:
1. Open Telegram and search for @BotFather
2. Send /newbot and follow the instructions
3. Copy the bot token you receive
""")

    config["telegram_bot_token"] = get_input(
        "Enter your Telegram bot token",
        is_secret=True,
    )

    print("""
Enter your channel ID. This can be:
- Channel username: @mychannel
- Channel ID: -1001234567890 (for private channels)
""")

    config["telegram_channel_id"] = get_input(
        "Enter your channel ID or username",
    )

    # Posting mode
    print_section("Posting Mode")

    print("""
Choose how to post to your channel:

1. Bot Mode (recommended)
   - Posts show "via @yourbot" label
   - Safe and official
   - No ban risk

2. Telethon Mode (advanced)
   - Posts appear as regular user messages
   - Requires dedicated Telegram account
   - Risk of account ban if detected
""")

    mode = get_input("Posting mode", default="bot")
    config["posting_mode"] = "telethon" if mode.lower() in ("2", "telethon", "user") else "bot"

    if config["posting_mode"] == "telethon":
        print("\nTelethon requires API credentials from https://my.telegram.org")
        config["telethon_api_id"] = get_input("API ID", required=False)
        config["telethon_api_hash"] = get_input("API Hash", required=False, is_secret=True)
        config["telethon_phone"] = get_input("Phone number (with country code)", required=False)

    # LLM Configuration
    print_section("LLM Configuration")

    print("""
Choose your LLM provider:

1. OpenAI (gpt-4o, gpt-4o-mini)
2. Anthropic Claude (claude-3-opus, claude-3-sonnet)
3. DeepSeek
""")

    provider_choice = get_input("LLM provider", default="1")
    provider_map = {"1": "openai", "2": "claude", "3": "deepseek"}
    config["llm_provider"] = provider_map.get(provider_choice, "openai")

    if config["llm_provider"] == "openai":
        config["llm_model"] = get_input("Model name", default="gpt-4o")
        config["openai_api_key"] = get_input("OpenAI API key", is_secret=True)
    elif config["llm_provider"] == "claude":
        config["llm_model"] = get_input("Model name", default="claude-3-5-sonnet-20241022")
        config["anthropic_api_key"] = get_input("Anthropic API key", is_secret=True)
    else:
        config["llm_model"] = get_input("Model name", default="deepseek-chat")
        config["deepseek_api_key"] = get_input("DeepSeek API key", is_secret=True)

    # Channel configuration
    print_section("Channel Content Settings")

    config["channel_topic"] = get_input(
        "Channel topic/niche",
        default="AI tools and automation for business",
    )

    config["channel_style"] = get_input(
        "Writing style",
        default="Expert practitioner. No hype. Real cases. Telegram-friendly.",
    )

    config["language"] = get_input(
        "Content language (ru/en)",
        default="ru",
    )

    # Schedule configuration
    print_section("Posting Schedule")

    print("""
Choose schedule type:

1. Fixed times - Post at specific times each day
2. Interval - Post every N hours
3. Random - Post at random times within a window
""")

    schedule_choice = get_input("Schedule type", default="1")
    schedule_map = {"1": "fixed", "2": "interval", "3": "random"}
    config["schedule_type"] = schedule_map.get(schedule_choice, "fixed")

    if config["schedule_type"] == "fixed":
        config["fixed_times"] = get_list(
            "Posting times (HH:MM format)",
            default=["09:30", "14:00", "20:00"],
        )
    elif config["schedule_type"] == "interval":
        config["interval_hours"] = get_input("Hours between posts", default="4")

    config["timezone"] = get_input("Timezone", default="Europe/Moscow")

    # Sources configuration
    print_section("Content Sources")

    print("""
Enter RSS feed URLs for content sources.
The system will collect topics from these feeds.
""")

    config["rss_feeds"] = get_list(
        "RSS feed URLs",
        default=[
            "https://feeds.feedburner.com/oreilly/radar",
            "https://hnrss.org/frontpage",
        ],
    )

    # Safety settings
    print_section("Safety Settings")

    config["manual_approval"] = get_bool(
        "Enable manual approval before posting?",
        default=False,
    )

    # Admin settings
    print_section("Admin Notifications")

    print("""
Enter your Telegram user ID for admin notifications.
You can get your ID from @userinfobot on Telegram.
""")

    config["admin_telegram_id"] = get_input(
        "Your Telegram user ID",
        required=False,
    )

    # Create files
    print_section("Creating Configuration Files")

    create_directories()
    create_env_file(config)
    create_config_yaml(config)

    # Test connection
    print_section("Testing Connection")

    print("Testing Telegram bot connection...")
    await test_telegram_connection(
        config["telegram_bot_token"],
        config["telegram_channel_id"],
    )

    # Final steps
    print_header("Setup Complete!")

    print("""
Configuration files created:
  - .env (contains secrets - DO NOT commit to git!)
  - config.yaml (contains settings)

Next steps:

1. Review and adjust settings in config.yaml

2. Initialize the database:
   python main.py --init-db

3. Test with dry run:
   python main.py --dry-run --once

4. Start the scheduler:
   python main.py

For more options:
   python main.py --help

Happy posting!
""")


def main() -> None:
    """Main entry point."""
    try:
        asyncio.run(run_setup_wizard())
    except KeyboardInterrupt:
        print("\n\nSetup cancelled.")
        sys.exit(1)
    except Exception as e:
        print(f"\nError during setup: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()
