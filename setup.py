#!/usr/bin/env python3
"""
Setup wizard for TG AI Poster.

Interactive first-run configuration wizard that helps users
set up their Telegram bot and channel settings.
"""

from __future__ import annotations

import asyncio
import os
import sys
from pathlib import Path
from typing import Optional

import yaml


def print_header(text: str) -> None:
    """Print a styled header."""
    print("\n" + "=" * 60)
    print(f"  {text}")
    print("=" * 60 + "\n")


def print_section(text: str) -> None:
    """Print a section header."""
    print(f"\n>>> {text}")
    print("-" * 40)


def get_input(
    prompt: str,
    default: Optional[str] = None,
    required: bool = True,
    is_secret: bool = False,
) -> str:
    """
    Get user input with optional default value.

    Args:
        prompt: Prompt text
        default: Default value
        required: Whether input is required
        is_secret: Whether to hide input

    Returns:
        str: User input
    """
    if default:
        prompt_text = f"{prompt} [{default}]: "
    else:
        prompt_text = f"{prompt}: "

    while True:
        try:
            if is_secret:
                import getpass
                value = getpass.getpass(prompt_text)
            else:
                value = input(prompt_text).strip()

            if not value:
                if default:
                    return default
                if not required:
                    return ""
                print("This field is required. Please enter a value.")
                continue

            return value

        except KeyboardInterrupt:
            print("\n\nSetup cancelled.")
            sys.exit(1)


def get_bool(prompt: str, default: bool = True) -> bool:
    """Get yes/no input."""
    default_str = "Y/n" if default else "y/N"
    while True:
        value = input(f"{prompt} [{default_str}]: ").strip().lower()
        if not value:
            return default
        if value in ("y", "yes", "true", "1"):
            return True
        if value in ("n", "no", "false", "0"):
            return False
        print("Please enter 'y' or 'n'.")


def get_list(prompt: str, default: list[str] = None) -> list[str]:
    """Get a list of values."""
    default_str = ", ".join(default) if default else ""
    print(f"{prompt}")
    print(f"Enter items separated by commas (or leave empty for default)")
    value = input(f"[{default_str}]: ").strip()

    if not value:
        return default or []

    return [item.strip() for item in value.split(",") if item.strip()]


def create_env_file(config: dict) -> None:
    """Create .env file from configuration."""
    env_path = Path(".env")

    if env_path.exists():
        if not get_bool(".env already exists. Overwrite?", default=False):
            print("Skipping .env creation.")
            return

    env_content = f"""# TG AI Poster Environment Variables
# Generated by setup wizard
# {"=" * 50}

# Telegram Bot
TELEGRAM_BOT_TOKEN={config.get("telegram_bot_token", "")}
TELEGRAM_CHANNEL_ID={config.get("telegram_channel_id", "")}

# Telethon (optional - for user account mode)
TELETHON_API_ID={config.get("telethon_api_id", "")}
TELETHON_API_HASH={config.get("telethon_api_hash", "")}
TELETHON_PHONE={config.get("telethon_phone", "")}

# LLM API Keys
OPENAI_API_KEY={config.get("openai_api_key", "")}
ANTHROPIC_API_KEY={config.get("anthropic_api_key", "")}
DEEPSEEK_API_KEY={config.get("deepseek_api_key", "")}

# Admin
ADMIN_TELEGRAM_ID={config.get("admin_telegram_id", "")}
"""

    env_path.write_text(env_content)
    print(f"Created {env_path}")


